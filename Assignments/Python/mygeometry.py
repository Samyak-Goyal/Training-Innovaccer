def CircleArea():
    radius=float(input("Enter Radius: "))
    area=3.14*radius**2
    print("Area of circle = %.2f" %area)

def RectangleArea(length,breadth):
    area= length*breadth
    print("Area of rectangle = %.2f" %area)

def TriangleArea(base,height):
    area= .5*base*height
    return area
