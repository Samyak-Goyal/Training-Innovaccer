function calc(a,b, opr){
    switch(opr){
        case '+': console.log(a+b);
        break;
        case '-': console.log(a-b);
        break;
        case '*': console.log(a*b);
        break;
        case '/': console.log(a/b);
        break;
        default: console.log("Invalid operator");
    }
}


